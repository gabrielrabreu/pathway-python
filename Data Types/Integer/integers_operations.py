# Integer Operations in Python

# Integers are fundamental data types in Python used to represent whole numbers. Here are some common integer operations:

# Creating integers
num1 = 10
num2 = 5

# Arithmetic Operations

# Addition: Add two integers
addition_result = num1 + num2
# Result: 15

# Subtraction: Subtract one integer from another
subtraction_result = num1 - num2
# Result: 5

# Multiplication: Multiply two integers
multiplication_result = num1 * num2
# Result: 50

# Division: Divide one integer by another (returns a float)
division_result = num1 / num2
# Result: 2.0

# Floor Division: Divide and discard the fractional part (returns an integer)
floor_division_result = num1 // num2
# Result: 2

# Modulus: Get the remainder of division
modulus_result = num1 % num2
# Result: 0

# Exponentiation: Calculate the power of an integer
exponentiation_result = num1 ** num2
# Result: 100000
