# Data Types in Python

# In Python, data types are used to classify and represent different types of data.
# Python supports several built-in data types, including:

# Integer (int): Whole numbers, e.g., 5, -10, 1000
x = 5

# Float (float): Decimal numbers, e.g., 3.14, -0.5, 1.0
y = 3.14

# String (str): Textual data, enclosed in single or double quotes, e.g., "Hello", 'Python'
name = "John"

# Boolean (bool): Represents True or False values, often used for logical operations
is_valid = True

# List (list): Ordered collection of items, mutable (can be modified)
numbers = [1, 2, 3]

# Tuple (tuple): Ordered collection of items, immutable (cannot be modified)
coordinates = (10, 20)

# Dictionary (dict): Collection of key-value pairs, where each key is unique
person = {"name": "Alice", "age": 30}

# Set (set): Unordered collection of unique items
unique_numbers = {1, 2, 3}

# You can check the type of a variable using the 'type' function:
# For example:
# print(type(x))  # Output: <class 'int'>
# print(type(name))  # Output: <class 'str'>

# Python also allows type conversion between different data types, such as converting an integer to a float or vice versa.
