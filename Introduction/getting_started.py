# Introduction to Python - Getting Started

# Python Installation

# You can download Python from the official website: https://www.python.org/downloads/
# Choose the appropriate version for your operating system (Windows, macOS, Linux) and follow the installation instructions.

# Verifying Python Installation
# To check if Python is installed correctly, open your terminal or command prompt and type the following command:
# python --version

# If installed successfully, you should see the Python version printed in the terminal.

# Python Interactive Shell
# Python provides an interactive shell where you can execute Python code line by line.
# To open the Python shell, open your terminal or command prompt and type:
# python

# You should see the Python shell prompt (">>>"), indicating that you can start entering Python code.

# Simple Python Code Example

print("Hello, Python!")

# Run it using the Python interpreter:
# python getting_started.py

# You should see "Hello, Python!" printed to the console.

